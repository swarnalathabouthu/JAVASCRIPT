//1.Print 10 numbers.
//A.for Loop.
for (var i=1;i<=10;++i){
    console.log(i)
 }
//B.While Loop
var i = 1;
while (i <= 10) {
    console.log(i);
    i++;
}
//C.Do-while Loop:
var i = 1;
do {
    console.log(i);
    i++;
} while (i <= 10);

